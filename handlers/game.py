def xp_for_level(level: int) -> int:
    return int(_LEVEL_BASE * (_LEVEL_MULT ** (level - 1)))


def parse_level(total_xp: int) -> tuple[int, int, int]:
    level, xp = 1, total_xp
    while True:
        needed = xp_for_level(level)
        if xp < needed:
            return level, xp, needed
        xp -= needed
        level += 1


def xp_bar(xp_in: int, xp_needed: int, width: int = 10) -> str:
    filled = math.floor((xp_in / xp_needed) * width) if xp_needed else 0
    return "█" * filled + "░" * (width - filled)


def streak_multiplier(streak: int) -> float:
    """Бонус +10% XP за каждые 7 дней серии, максимум +50%."""
    return 1.0 + min(streak // 7, 5) * 0.10


def streak_label(streak: int) -> str:
    if streak == 0:
        return ""
    mult = streak_multiplier(streak)
    bonus = f" (+{int((mult - 1) * 100)}% XP)" if mult > 1.0 else ""
    fires = "🔥" * min(streak // 7 + 1, 5)
    if streak == 1:
        return "\n🌅 <b>Серия дней начата!</b> Заходи каждый день — серия растёт и даёт бонус XP."
    return f"\n{fires} <b>Серия дней: {streak} дн.{bonus}</b>"


