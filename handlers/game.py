@router.callback_query(F.data.startswith("game:quest:"))
async def game_quest(callback: CallbackQuery, state: FSMContext):
    if callback.from_user.id not in ADMIN_IDS:
        await callback.answer()
        return

    parts = callback.data.split(":")
    loc_id, quest_id = parts[2], parts[3]
    loc = LOCATIONS.get(loc_id) or WORLD_LOCATIONS.get(loc_id)
    quest = next((q for q in loc["quests"] if q["id"] == quest_id), None) if loc else None
    if not quest:
        await callback.answer("Квест не найден", show_alert=True)
        return

    quest = _shuffle_quest_options(quest)

    completed = await game_get_completed_quests(callback.from_user.id)
    already_done = quest_id in completed

    player = await game_get_or_create_player(callback.from_user.id)
    hints = player.get("hint_charges", 0)
    boosts = player.get("xp_boost_charges", 0)

    state_data = await state.get_data()
    legendary = state_data.get("legendary", False)

    eliminated = None
    if not already_done and hints > 0 and not legendary:
        eliminated = _pick_eliminated(quest["options"], quest["correct"])
        await game_use_hint(callback.from_user.id)

    boost_line = "\n⚡ <b>Бонус x2 XP активен!</b>" if (not already_done and boosts > 0) else ""
    hint_line = "\n💡 <i>Подсказка применена — один неверный вариант исключён</i>" if eliminated is not None else ""
    done_line = "\n\n✅ <i>Квест пройден. Ответь ещё раз для повторения (XP не начисляется).</i>" if already_done else ""

    opts = _options_text(quest["options"], quest["correct"], None, eliminated)
    text = (
        f"{loc['emoji']} <b>{loc['name']}</b> › {html.escape(quest['title'])}\n\n"
        f"<i>{html.escape(quest['story'])}</i>\n\n"
        f"❓ <b>{html.escape(quest['question'])}</b>\n\n"
        f"{opts}"
        f"{boost_line}{hint_line}{done_line}"
    )

    kb = _quest_options_kb(loc_id, quest_id, quest["options"], quest["correct"], None, eliminated)
    await callback.message.edit_text(text, parse_mode="HTML", reply_markup=kb)
    await state.set_state(GameState.in_quest)
    await state.update_data(
        loc_id=loc_id, quest_id=quest_id, eliminated=eliminated,
        shuffled_correct=quest["correct"], shuffled_options=quest["options"],
    )
    await callback.answer()


