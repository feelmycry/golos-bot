def _pick_random_quest() -> tuple[str, str, dict]:
    """Returns (loc_id, quest_id, quest_dict)."""
    all_quests = []
    for loc_id, loc in {**LOCATIONS, **WORLD_LOCATIONS}.items():
        for q in loc["quests"]:
            all_quests.append((loc_id, q["id"], q))
    return random.choice(all_quests)


