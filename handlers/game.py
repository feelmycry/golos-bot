def _time_until_reset() -> str:
    now = datetime.utcnow()
    tomorrow = (now + timedelta(days=1)).replace(hour=0, minute=0, second=0, microsecond=0)
    delta = tomorrow - now
    h, m = delta.seconds // 3600, (delta.seconds % 3600) // 60
    return f"{h}ч {m}мин"


def _stock_price(loc_id: str, base: int, for_date: date | None = None, apply_event: bool = True) -> int:
    d = for_date or date.today()
    h = int(hashlib.sha256(f"{d.isoformat()}:{loc_id}".encode()).hexdigest()[:8], 16)
    pct = 85 + (h % 31)  # 85%–115% от базы
    price = max(1, int(base * pct / 100))
    if apply_event and for_date is None:
        price = max(1, int(price * _get_event_modifier(loc_id)))
    return price


def _stock_info(loc_id: str) -> tuple[int, float]:
    """Возвращает (цена_сегодня, изменение_в_%)."""
    cfg = STOCK_CONFIG.get(loc_id)
    if not cfg:
        return 0, 0.0
    today = _stock_price(loc_id, cfg["base_price"])
    yesterday = _stock_price(loc_id, cfg["base_price"], date.today() - timedelta(days=1))
    change = (today - yesterday) / yesterday * 100
    return today, change


