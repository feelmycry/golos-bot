@router.callback_query(F.data.startswith("game:answer:"))
async def game_answer(callback: CallbackQuery, state: FSMContext):
    if callback.from_user.id not in ADMIN_IDS:
        await callback.answer()
        return

    parts = callback.data.split(":")
    loc_id, quest_id, answer_idx = parts[2], parts[3], int(parts[4])
    loc = LOCATIONS.get(loc_id)
    quest = next((q for q in loc["quests"] if q["id"] == quest_id), None) if loc else None
    if not quest:
        await callback.answer("Квест не найден", show_alert=True)
        return

    state_data = await state.get_data()
    eliminated = state_data.get("eliminated")

    is_correct = answer_idx == quest["correct"]
    completed = await game_get_completed_quests(callback.from_user.id)
    already_done = quest_id in completed

    player_before = await game_get_or_create_player(callback.from_user.id)
    level_before = parse_level(player_before["xp"])[0]
    streak = player_before.get("streak_days", 0) or 0
    mult = streak_multiplier(streak)

    xp_gained = 0
    coins_gained = 0
    rep_gained = 0

    if is_correct and not already_done:
        xp_raw = quest["xp"]
        boosts = player_before.get("xp_boost_charges", 0)
        if boosts > 0:
            xp_raw = int(xp_raw * 2)
            await game_use_xp_boost(callback.from_user.id)
        xp_gained = int(xp_raw * mult)
        coins_gained = quest["coins"]
        rep_gained = quest["rep"]
        await game_save_quest_result(callback.from_user.id, quest_id, loc_id, xp_gained, coins_gained, rep_gained)
    elif is_correct:
        rep_gained = quest["rep"] // 3
        await game_save_quest_result(callback.from_user.id, quest_id, loc_id, 0, 0, rep_gained)

    player_after = await game_get_or_create_player(callback.from_user.id)
    level_after = parse_level(player_after["xp"])[0]
    _, xp_in, xp_need = parse_level(player_after["xp"])

    level_up_text = ""
    if level_after > level_before:
        unlock = LEVEL_UNLOCKS.get(level_after, "")
        unlock_line = f"\n✨ {unlock}" if unlock else ""
        level_up_text = f"\n\n🎉 <b>УРОВЕНЬ {level_after}!{unlock_line}</b>"

    streak_bonus_text = ""
    if is_correct and not already_done and mult > 1.0:
        streak_bonus_text = f" (×{mult:.1f} серия)"

    if is_correct:
        result_header = "✅ <b>Верно!</b>"
        reward_line = (
            f"\n\n🎁 +{xp_gained} XP{streak_bonus_text}, +{coins_gained} ИР, +{rep_gained} реп."
            if not already_done else
            f"\n\n🔄 <i>Повтор: +{rep_gained} репутации</i>"
        )
    else:
        result_header = "❌ <b>Неверно.</b>"
        reward_line = ""

    text = (
        f"{loc['emoji']} <b>{loc['name']}</b> › {quest['title']}\n\n"
        f"{result_header}\n\n"
        f"💡 <b>Объяснение:</b>\n{quest['explanation']}"
        f"{reward_line}"
        f"{level_up_text}\n\n"
        f"📊 Уровень {level_after} | XP {xp_in}/{xp_need}"
    )

    kb = _quest_options_kb(loc_id, quest_id, quest["options"], quest["correct"], answer_idx, eliminated)
    await callback.message.edit_text(text, parse_mode="HTML", reply_markup=kb)
    await state.clear()
    await callback.answer("✅ Верно!" if is_correct else "❌ Неверно")


