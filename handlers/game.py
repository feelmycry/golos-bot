@router.callback_query(F.data == "game:open")
async def game_open(callback: CallbackQuery, state: FSMContext):
    if callback.from_user.id not in ADMIN_IDS:
        await callback.answer("🚧 Игра находится в разработке. Скоро откроем доступ!", show_alert=True)
        return

    await state.clear()
    player = await game_get_or_create_player(callback.from_user.id)

    if not player.get("onboarded", 1):
        await game_set_onboarded(callback.from_user.id)
        b = InlineKeyboardBuilder()
        b.button(text="🚀 Начать играть!", callback_data="game:open")
        b.adjust(1)
        await callback.message.edit_text(_HOWTOPLAY_TEXT, parse_mode="HTML", reply_markup=b.as_markup())
        await callback.answer()
        return

    streak = await game_update_streak(callback.from_user.id)

    level, xp_in, xp_need = parse_level(player["xp"])
    bar = xp_bar(xp_in, xp_need)
    unlock = LEVEL_UNLOCKS.get(level, "")
    unlock_line = f"\n🎁 <b>{unlock}</b>" if unlock else ""

    hints = player.get("hint_charges", 0)
    boosts = player.get("xp_boost_charges", 0)
    inv_line = ""
    if hints or boosts:
        parts = []
        if hints:
            parts.append(f"💡 Подсказок: {hints}")
        if boosts:
            parts.append(f"⚡ Бустов XP: {boosts}")
        inv_line = "\n" + " · ".join(parts)

    events = _get_today_events()
    events_line = "\n".join(f"  {ev['emoji']} {ev['text']}" for ev in events)

    text = (
        f"🎮 <b>ИНВЕСТОР: ВОСХОЖДЕНИЕ</b>\n"
        f"<i>Стань легендой фондового рынка России</i>\n\n"
        f"🎖️ Уровень <b>{level}</b> | XP: {xp_in}/{xp_need}\n"
        f"[{bar}]{unlock_line}\n"
        f"💰 ИнвестРубли: <b>{player['coins']:,}</b>"
        f"{streak_label(streak)}"
        f"{inv_line}\n\n"
        f"📰 <b>События дня:</b>\n{events_line}\n\n"
        f"Выбери действие:"
    )
    await callback.message.edit_text(text, parse_mode="HTML", reply_markup=_game_main_kb())
    await callback.answer()


