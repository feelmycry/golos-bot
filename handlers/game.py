def _location_kb(loc_id: str, quests: list, completed: set) -> object:
    b = InlineKeyboardBuilder()
    for q in quests:
        done = q["id"] in completed
        b.button(
            text=f"{'✅' if done else '📋'} {q['title']}",
            callback_data=f"game:quest:{loc_id}:{q['id']}",
        )
    b.button(text="◀️ Назад к карте", callback_data="game:map")
    b.adjust(1)
    return b.as_markup()


def _quest_options_kb(
    loc_id: str,
    quest_id: str,
    options: list,
    correct: int,
    answered: int | None,
    eliminated: int | None = None,
) -> object:
    b = InlineKeyboardBuilder()
    for i, opt in enumerate(options):
        if answered is None:
            if eliminated == i:
                label = f"🚫 {opt}"
                cb = "game:noop"
            else:
                label = f"{chr(0x31 + i)}️⃣ {opt}"
                cb = f"game:answer:{loc_id}:{quest_id}:{i}"
        else:
            if i == correct:
                label = f"✅ {opt}"
            elif i == answered:
                label = f"❌ {opt}"
            else:
                label = f"   {opt}"
            cb = "game:noop"
        b.button(text=label, callback_data=cb)
    if answered is not None:
        b.button(text="▶️ Продолжить", callback_data=f"game:location:{loc_id}")
    b.adjust(1)
    return b.as_markup()


def _get_rank(loc: dict, rep: int) -> str:
    rank = loc["rep_rank"][0]
    for i, thresh in enumerate(loc["rep_thresholds"]):
        if rep >= thresh:
            rank = loc["rep_rank"][i]
    return rank


def _calc_pending(last_collected: str | None, income_per_hour: int) -> int:
    if not last_collected or income_per_hour == 0:
        return 0
    try:
        last = datetime.fromisoformat(last_collected)
        hours = min((datetime.utcnow() - last).total_seconds() / 3600, 12)
        return int(hours * income_per_hour)
    except Exception:
        return 0


def _pick_eliminated(options: list, correct: int) -> int:
    wrong = [i for i in range(len(options)) if i != correct]
    return wrong[-1]


