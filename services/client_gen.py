_AGE_RANGES = {
    "young": (22, 34),
    "middle": (35, 49),
    "adult": (50, 59),
    "pensioner": (60, 75),
}

_BALANCE_RANGES = {
    "young": (30_000, 600_000),
    "middle": (150_000, 3_500_000),
    "adult": (300_000, 6_000_000),
    "pensioner": (80_000, 2_500_000),
}


def generate_client(cohort: str, scenario: str) -> dict:
    gender = random.choice(["male", "female"])
    first_name = random.choice(_NAMES[cohort][gender])

    if cohort in ("adult", "pensioner"):
        patronymic = random.choice(_PATRONYMICS[gender])
        full_name = f"{first_name} {patronymic}"
    else:
        full_name = first_name

    age = random.randint(*_AGE_RANGES[cohort])

    raw_balance = random.randint(*_BALANCE_RANGES[cohort])
    balance = (raw_balance // 1000) * 1000

    has_debit = random.random() > 0.15
    has_credit = random.random() > 0.55

    products_parts = []
    if has_debit:
        products_parts.append("дебетовая карта Альфа-Банка")
    if has_credit:
        products_parts.append("кредитная карта Альфа-Банка")
    products_str = ", ".join(products_parts) if products_parts else "нет банковских карт"

    appearance = random.choice(_APPEARANCE[cohort])
    mood = random.choice(_MOODS)
    purpose = random.choice(_PURPOSES[scenario])

    return {
        "name": full_name,
        "age": age,
        "cohort": cohort,
        "gender": gender,
        "balance": balance,
        "has_debit": has_debit,
        "has_credit": has_credit,
        "products": products_str,
        "appearance": appearance,
        "mood": mood,
        "purpose": purpose,
    }
